$(".plus").on("click", function () {
  if ($(".type").css("display") == "none") {
    $(".plus i").css("transform", "rotate(45deg)");
    $(".type").slideDown();
  } else {
    $(".plus i").css("transform", "rotate(0deg)");
    $(".type").slideUp();
  }
});
